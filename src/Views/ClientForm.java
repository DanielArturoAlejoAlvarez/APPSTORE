    private void tbClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbClientMouseClicked
       int row = tbClient.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please selected a client!");
        }else {
            id = Integer.parseInt(tbClient.getValueAt(row, 0).toString());
            String dni = tbClient.getValueAt(row, 1).toString();
            String fullname = tbClient.getValueAt(row, 2).toString();
            String address = tbClient.getValueAt(row, 3).toString();
            String state = cboState.getSelectedItem().toString();
            
            txtDni.setText(dni);
            txtFullname.setText(fullname);
            txtAddress.setText(address);
            cboState.setSelectedItem(state);           
        }
    }//GEN-LAST:event_tbClientMouseClicked

    void add() {
        String dni = txtDni.getText();
        String fullname = txtFullname.getText();
        String address = txtAddress.getText();
        String state = cboState.getSelectedItem().toString();
        
        Object[] obj = new Object[4];
        obj[0] = dni;
        obj[1] = fullname;
        obj[2] = address;
        obj[3] = state;
        
        cdao.add(obj);
        
    }
    
    void update() {
        int row = tbClient.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please selected a client!");
        }else  {
            String dni = txtDni.getText();
            String fullname = txtFullname.getText();
            String address = txtAddress.getText();
            String state = cboState.getSelectedItem().toString();

            Object[] obj = new Object[5];
            obj[0] = dni;
            obj[1] = fullname;
            obj[2] = address;
            obj[3] = state;
            obj[4] = id;

            cdao.update(obj);
        }
        
    }
    
    void delete() {
        int row = tbClient.getSelectedRow();
        int ids = Integer.parseInt(tbClient.getValueAt(row, 0).toString());
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please selected a client!");
        }else  {
            cdao.delete(ids);
        }
    }
    
    void reset() {
        txtDni.setText("");
        txtFullname.setText("");
        txtAddress.setText("");
        cboState.setSelectedItem(0);
        txtDni.requestFocus();
    }
    
    void clearTable() {
        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
            i = i - 1;
        }
    }

