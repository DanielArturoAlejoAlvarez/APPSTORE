    void clientList() {
        List<Client> clients = cdao.list();
        model = (DefaultTableModel)tbClient.getModel();
        Object[] obj = new Object[5];
        for (int i = 0; i < clients.size(); i++) {
            obj[0] = clients.get(i).getId();
            obj[1] = clients.get(i).getDni();
            obj[2] = clients.get(i).getFullnane();
            obj[3] = clients.get(i).getAddress();
            obj[4] = clients.get(i).getState();            
            model.addRow(obj);
        }
        tbClient.setModel(model);
    }

